package basics;

public class datatypes {
    public static void main(String[] args) {
        // byte - 1[-128 to 127]
        // short -2
        // int -4
        // long - 8
        // float - 4 3.14
        // double - 8
        // char - 2,a,b,c,d
        // boolean -1 true / false
        
        // for declaring the long variable we must use the 'L' at the end of the no to
        // tell the java that we are using a long type integer.
        
        
        byte age = 12;
        int phone = 1234567890;
        long phone2 = 12345678900L;
        float pi = 3.14F;
        char letter = '@';
        boolean isAdult = false;

    }
}